public class RecordMapInfo {

    private String mapArea;
    private int walkwayTotal;
    private int walkwayVisited;

    public void setMapArea(String mapArea) {
        this.mapArea = mapArea;
    }

    public void setWalkwayTotal(int walkwayTotal) {
        this.walkwayTotal = walkwayTotal;
    }

    public void setWalkwayVisited(int walkwayVisited) {
        this.walkwayVisited = walkwayVisited;
    }

    public String getMapArea() {
        return mapArea;
    }

    public int getWalkwayTotal() {
        return walkwayTotal;
    }

    public int getWalkwayVisited() {
        return walkwayVisited;
    }


}
